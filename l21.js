function sum1(n) {
    let sum = 0;
    for (let i = 1; i <= n; i++) {
        sum += i;
    }
    return sum;
}

alert(sum1(100));

function sum2(n) {
    if (n == 1) {
        return 1;
    }
    return n + sum2(n - 1);
}

alert(sum2(100))